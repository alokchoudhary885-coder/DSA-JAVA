public class SwitchCaseExample {

public static void main (String args[]){
    int number = 1;
    switch (number){
        case 1: System.out.println("alok");
            break;
        case 2: System.out.println("AKASH");
            break;
        case 3: System.out.println("Ankit");
            break;
        default: System.out.println("Invalid");
    }
}
}
    

